    <h2>1. Запуск веб-приложения</h2>
    <p>Для того, чтобы открыть программный продукт, необходимо в терминале выполнить команду:</p>
    <pre><code>php artisan serve</code></pre>
    <p>Запустится локальный сервер, и программный продукт будет доступен по адресу:</p>
    <pre><code>http://127.0.0.1:8001</code></pre>

